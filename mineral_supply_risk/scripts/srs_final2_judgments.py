    i = sum(1 for v in JUDGMENTS.values() if v == "I")
    u = sum(1 for v in JUDGMENTS.values() if v == "U")
    n = r + i

    def _wilson_ci(k, n, z=1.96):
        if n == 0:
            return (0.0, 0.0)
        p = k / n
        denom = 1 + z**2 / n
        center = p + z**2 / (2 * n)
        half = z * (p * (1 - p) / n + z**2 / (4 * n**2)) ** 0.5
        return ((center - half) / denom, (center + half) / denom)

    lo, hi = _wilson_ci(i, n)
    print(f"R={r} I={i} U={u} (합계 {r+i+u})")
    print(f"오염률(U 제외 n={n}) = {i}/{n} = {i/n:.1%} (95% CI [{lo:.1%}, {hi:.1%}])")
    print(f"관련성(유효성) = {1 - i/n:.1%} (95% CI [{1-hi:.1%}, {1-lo:.1%}])")
