    def _wilson_ci(k, n, z=1.96):
        if n == 0:
            return (0.0, 0.0)
        p = k / n
        denom = 1 + z**2 / n
        center = p + z**2 / (2 * n)
        half = z * (p * (1 - p) / n + z**2 / (4 * n**2)) ** 0.5
        return ((center - half) / denom, (center + half) / denom)

    lo, hi = _wilson_ci(i, n)
